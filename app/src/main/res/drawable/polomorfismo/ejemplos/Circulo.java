package com.dhenis.polomorfismo.ejemplos;

public class Circulo {
    Integer radio;
    Double pi;

    public Circulo(Integer radio, Double pi) {
        this.radio = radio;
        this.pi = pi;
    }

    public Integer getRadio() {
        return radio;
    }

    public Double getPi() {
        return pi;
    }
}
