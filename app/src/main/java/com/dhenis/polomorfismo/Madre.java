package com.dhenis.polomorfismo.ejemplos;

public class Madre {
    Integer lado1;
    Integer lado2;

    public Madre(Integer lado1, Integer lado2) {
        this.lado1 = lado1;
        this.lado2 = lado2;
    }

    public Integer getLado1() {
        return lado1;
    }

    public void setLado1(Integer lado1) {
        this.lado1 = lado1;
    }

    public Integer getLado2() {
        return lado2;
    }

    public void setLado2(Integer lado2) {
        this.lado2 = lado2;
    }
}
